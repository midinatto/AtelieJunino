   private void load() {
    List<Aluguel> alu = aluguelDao.pesquisar();

    DefaultTableModel model = new javax.swing.table.DefaultTableModel(
        new String[]{"Id", "Observacao", "Valor", "Adiantamento", "Restante",
                     "Número Traje", "Data Quadrilha", "Data Reserva", "Data Devolucao",
                     "Acompanhamentos", "Escola", "Atendente"}, 0);

    for (Aluguel a : alu) {
        model.addRow(new Object[]{
            a.getId(), a.getObservacao(), a.getValor(), a.getAdiantamento(),
            a.getRestante(), a.getNumeroTraje(), a.getDataQuadrilha(),
            a.getDataReserva(), a.getDataDevolucao(), a.getAcompanhamentos(),
            a.getEscola(), a.getAtendente()
        });
    }

    AluguelTable.setModel(model);
}

