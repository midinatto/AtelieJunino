    private void EditarBut1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarBut1ActionPerformed
        if (ClienteTable.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um cliente para editar.");
        } else {
            int clienteId = (int) ClienteTable.getValueAt(ClienteTable.getSelectedRow(), 0);
            new EditarCliente(clienteId).setVisible(true);
            this.dispose();
        } // TODO add your handling code here:
    }//GEN-LAST:event_EditarBut1ActionPerformed

    private void ExcluirBut1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirBut1ActionPerformed
        if (ClienteTable.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um especialidade para excluir.");
        } else {
            int id = (int) ClienteTable.getValueAt(ClienteTable.getSelectedRow(), 0);
            try {
                clienteDao.excluir(id);
            } catch (SQLException ex) {
                Logger.getLogger(ListaCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
            load();
        } // TODO add your handling code here:
    }//GEN-LAST:event_ExcluirBut1ActionPerformed

