   private void load() {
    List<Traje> tra = trajeDao.pesquisar();

    TrajeTable.setModel(new javax.swing.table.DefaultTableModel(
        new Object[tra.size()][4],
        new String[]{"ID", "Descrição", "Valor", "Número do Traje"}
    ));

    for (int i = 0; i < tra.size(); i++) {
        TrajeTable.setValueAt(tra.get(i).getId(), i, 0);
        TrajeTable.setValueAt(tra.get(i).getDescricao(), i, 1);
        TrajeTable.setValueAt(tra.get(i).getValor(), i, 2);
        TrajeTable.setValueAt(tra.get(i).getNumeroTraje(), i, 3);
    }
}


