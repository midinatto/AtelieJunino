    private void ExcluirBut1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirBut1ActionPerformed
        if (TrajeTable.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um traje para excluir.");
        } else {
            int id = (int) TrajeTable.getValueAt(TrajeTable.getSelectedRow(), 0);
            trajeDao.excluir(id);
            load();
        } // TODO add your handling code here:
    }//GEN-LAST:event_ExcluirBut1ActionPerformed

    private void EditarBut1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarBut1ActionPerformed
        if (TrajeTable.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um traje para editar.");
        } else {
            int trajeId = (int) TrajeTable.getValueAt(TrajeTable.getSelectedRow(), 0);
            new EditarTraje(trajeId).setVisible(true);
            this.dispose();
        } // TODO add your handling code here:
    }//GEN-LAST:event_EditarBut1ActionPerformed

