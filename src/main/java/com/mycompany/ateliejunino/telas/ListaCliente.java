    private void load() {
    List<Cliente> cli = clienteDao.pesquisar();

    // Define o modelo da tabela com o número correto de linhas
    DefaultTableModel model = new javax.swing.table.DefaultTableModel(
        new Object[cli.size()][12],
        new String[]{"Id", "Nome Responsavel", "Nome Dancante", "Idade", "Rua", "Número", "Complemento", "Bairro", "Cidade", "Estado", "Telefone", "Escola"}
    );
    
    // Define o modelo da tabela
    ClienteTable.setModel(model);
    
    // Preenche os dados dos clientes na tabela
    for (int i = 0; i < cli.size(); i++) {
        Cliente cliente = cli.get(i);
        model.setValueAt(cliente.getId(), i, 0);
        model.setValueAt(cliente.getnomeResponsavel(), i, 1);
        model.setValueAt(cliente.getnomeDancante(), i, 2);
        model.setValueAt(cliente.getidade(), i, 3);
        model.setValueAt(cliente.getrua(), i, 4);
        model.setValueAt(cliente.getnum(), i, 5);
        model.setValueAt(cliente.getcomplemento(), i, 6);
        model.setValueAt(cliente.getbairro(), i, 7);
        model.setValueAt(cliente.getcidade(), i, 8);
        model.setValueAt(cliente.getestado(), i, 9);
        model.setValueAt(cliente.gettelefone(), i, 10);
        model.setValueAt(cliente.getescola(), i, 11);
    }
}

