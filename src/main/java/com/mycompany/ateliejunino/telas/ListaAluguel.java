    private void ExcluirBut1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirBut1ActionPerformed
        if (AluguelTable.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um Aluguel para excluir.");
        } else {
            int id = (int) AluguelTable.getValueAt(AluguelTable.getSelectedRow(), 0);
            try {
                aluguelDao.excluir(id);
            } catch (SQLException ex) {
                Logger.getLogger(ListaAluguel.class.getName()).log(Level.SEVERE, null, ex);
            }
            load();
        } // TODO add your handling code here:
    }//GEN-LAST:event_ExcluirBut1ActionPerformed

    private void EditarBut1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarBut1ActionPerformed
        if (AluguelTable.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um Aluguel para editar.");
        } else {
            int aluguelId = (int) AluguelTable.getValueAt(AluguelTable.getSelectedRow(), 0);
        new EditarAluguel(aluguelId).setVisible(true);
            this.dispose();
        } // TODO add your handling code here:
    }//GEN-LAST:event_EditarBut1ActionPerformed

